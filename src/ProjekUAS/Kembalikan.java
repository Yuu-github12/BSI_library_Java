    private void tampilDataP(){
        String[] kolom = {"KODE PINJAM","KODE ANGGOTA","NAMA","KODE BUKU","JUDUL BUKU","PENERBIT","PENGARANG","TAHUN TERBIT","TANGGAL PINJAM"};
        Object[][] objData = new Object[listpinjamB.size()][9];//jumlah kolom
        
        int i = 0;
        for(KembaliData n : listpinjamB){
            String[] arrData = {String.valueOf(n.getKpinjam()),n.getKang(),n.getNang(),n.getKbuku(),n.getJbuku(),n.getPenerbit(),n.getPengarang(), String.valueOf(n.getTTerbit()),n.getTanggalpin()};
            objData[i] = arrData;
            i++;
        }
        
        tabelModel = new DefaultTableModel(objData,kolom){
            //membuat tabel tidak bisa diedit saat diklik
            @Override
            public boolean isCellEditable(int rowIndex, int colIndex) {
                return false;
            }
        };
        
        TablePinjam.setModel(tabelModel);
        
        
    }

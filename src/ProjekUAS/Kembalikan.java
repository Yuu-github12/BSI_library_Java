    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
      int hari= 0;
      int denda = 2000;
      int hitungdenda;
      try{
                   String tanggalpin=txtTpin.getText();
                   String tanggalpen=txtTpen.getText();

      Date hari1 = new SimpleDateFormat("dd-MM-yyyy").parse(tanggalpin);
      Date hari2 = new SimpleDateFormat("dd-MM-yyyy").parse(tanggalpen);

      Calendar cal1=Calendar.getInstance();
      cal1.setTime(hari1);

      Calendar cal2=Calendar.getInstance();
      cal2.setTime(hari2);

      hari=daysBetween(cal1.getTime(),cal2.getTime());
      }
      catch(Exception ex){}
      
      if (hari>=4){
       hari=hari-3;
       hitungdenda=hari*denda;
       txtDenda.setText(""+hitungdenda);
       txtLambat.setText(""+hari+" hari");
      }
      else if(txtTpen.getText().equals("")){
          javax.swing.JOptionPane.showMessageDialog(this, "Masukan tanggal yang benar !!");
          txtLambat.setText("");
          txtDenda.setText("");
          txtTpen.requestFocus();
          
      }        
      else if(hari==3||hari==2||hari==1||hari==0){
         txtDenda.setText("tidak ada denda");
         txtLambat.setText("0 hari");
      }
      else if(hari<=-1){
         javax.swing.JOptionPane.showMessageDialog(this, "Masukan tanggal yang benar !!");
          txtTpen.setText("");
          txtLambat.setText("");
          txtDenda.setText("");
          txtTpen.requestFocus();
      }
      else{
          javax.swing.JOptionPane.showMessageDialog(this, "Masukan tanggal yang benar !!");
          txtTpen.setText("");
          txtLambat.setText("");
          txtDenda.setText("");
          txtTpen.requestFocus();
      }
    }//GEN-LAST:event_HitungActionPerformed

